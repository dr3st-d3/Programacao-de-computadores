# Exemplo 46 - mínimo, máximo e soma com listas
notas = [6, 7.5, 5.0, 8.5, 9.5]
linguagens = ["Pyhton", "Java", "C", 'C++', 'C#', 'JavaScript', 'Vb.net', 'Delphi', 'Cobol', 'Asembler', 'ZPL']

print(min(notas))
print(max(notas))
print(sum(notas))
print(len(notas))
print("\n")
print(min(linguagens))
print(max(linguagens))
print(len(linguagens))
