# Exemplo 47b - método reverse com listas
notas = [6.3, 7.5, 5.0, 8.5, 9.5, 6.0]
linguagens = ['Python', 'Java', 'C', 'C++', 'C#', 'JavaScript', 'Vb.net', 'Delphi', 'Cobol', 'Assembler', 'ZPL']

print(notas)
notas.reverse()
print(notas)
print("\n")
print(linguagens)
linguagens.reverse()
print(linguagens)
